function hasArg(name) {
  return process.argv.includes(name);
}

function readArg(name, fallback = "") {
  const index = process.argv.indexOf(name);
  return index === -1 ? fallback : process.argv[index + 1] || "";
}

function sha256(value) {
  return createHash("sha256").update(value).digest("hex");
}

function isoStamp(date = new Date()) {
  return date.toISOString().replace(/[:.]/g, "-");
}

export function patchRuntimeIdentityPromptSource(source) {
  let patched = source;
  const replacements = [];
  for (const [from, to] of PROMPT_REPLACEMENTS) {
    if (!patched.includes(from)) continue;
    const count = patched.split(from).length - 1;
    patched = patched.split(from).join(to);
    replacements.push({ from, to, count });
  }
  return { source: patched, replacements };
}

export async function patchRuntimeIdentityPromptFiles({
  distRoot,
  write = false,
  backupDir = "",
} = {}) {
  const names = await readdir(distRoot);
  const results = [];
  for (const name of names.filter((entry) => entry.endsWith(".js")).sort()) {
    const path = join(distRoot, name);
    const before = await readFile(path, "utf8");
    const patched = patchRuntimeIdentityPromptSource(before);
    if (patched.source === before) continue;
    if (write) {
      if (backupDir) {
        await mkdir(backupDir, { recursive: true });
        await copyFile(path, join(backupDir, basename(path)));
      }
      await writeFile(path, patched.source);
    }
    results.push({
      file: path,
      beforeSha256: sha256(before),
      afterSha256: sha256(patched.source),
      replacements: patched.replacements,
    });
  }
  return results;
}

export async function findInstalledCodexRuntimeDistRoots({
  stateHome = LIVE_STATE_HOME,
} = {}) {
  const projectsRoot = join(stateHome, "npm", "projects");
  if (!existsSync(projectsRoot)) return [];
  const projects = await readdir(projectsRoot, { withFileTypes: true });
  const roots = [];
  for (const project of projects) {
    if (!project.isDirectory()) continue;
    const distRoot = join(
      projectsRoot,
      project.name,
      "node_modules",
      "@openclaw",
      "codex",
      "dist",
    );
    if (existsSync(distRoot)) roots.push(distRoot);
  }
  return roots.sort();
}

export async function resolveLiveIdentityDistRoots({
  primaryDist = LIVE_DIST,
  stateHome = LIVE_STATE_HOME,
  extraDists = process.env.GPAO_T_RUNTIME_IDENTITY_EXTRA_DISTS || "",
} = {}) {
  const configured = extraDists
    .split(",")
    .map((value) => value.trim())
    .filter(Boolean);
  const installed = await findInstalledCodexRuntimeDistRoots({ stateHome });
  return [...new Set([primaryDist, ...configured, ...installed])]
    .filter((root) => existsSync(root));
}

async function main() {
  const apply = hasArg("--apply");
  const token = readArg("--token");
  if (apply && token !== APPLY_TOKEN) {
    throw new Error(`live apply requires --token ${APPLY_TOKEN}`);
  }

  const backupDir = apply ? join(BACKUP_ROOT, isoStamp()) : "";
  const liveDists = await resolveLiveIdentityDistRoots();
  const results = [];
  for (const [index, distRoot] of liveDists.entries()) {
    const rootBackupDir = backupDir
      ? join(backupDir, `runtime-${index + 1}-${sha256(distRoot).slice(0, 12)}`)
      : "";
    results.push(...await patchRuntimeIdentityPromptFiles({
      distRoot,
      write: apply,
      backupDir: rootBackupDir,
    }));
  }
  const report = {
    schema: "gpao_t.live_runtime_identity_prompt_patch.v0_1",
    status: apply ? "applied" : "dry_run",
    createdAt: new Date().toISOString(),
    liveDists,
    changedFileCount: results.length,
    backupDir: apply ? backupDir : null,
    results,
  };
  await mkdir(BACKUP_ROOT, { recursive: true });
  await writeFile(
    join(BACKUP_ROOT, apply ? "apply-result.json" : "dry-run-result.json"),
    `${JSON.stringify(report, null, 2)}\n`,
  );
  console.log(JSON.stringify(report, null, 2));
}

if (fileURLToPath(import.meta.url) === process.argv[1]) {
  main().catch((error) => {
    console.error(error instanceof Error ? error.stack || error.message : String(error));
    process.exitCode = 1;
  });
}
